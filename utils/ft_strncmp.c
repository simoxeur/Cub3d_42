#include "utils.h"

int	ft_strncmp(char *s1, char *s2, int n)
{
	if (!s1 || !s2 || n <= 0)
		return (0);
	while (*s1 == *s2 && n > 1)
	{
		n--;
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
